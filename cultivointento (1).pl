% Regla de recomendaci√≥n por suelo, clima y estaci√≥n
recomendar(Clima, TipoSuelo, Temporada, Resultado) :-
    findall(Cultivo, (cultivo(Cultivo, Clima, Suelos, Temporadas, _, _, _),
                      member(TipoSuelo, Suelos),
                      member(Temporada, Temporadas)), Resultados),
    (Resultados \= [] -> 
        atomic_list_concat(Resultados, ', ', ResultadoTexto),
        send(Resultado, selection, ResultadoTexto)
    ; 
        send(Resultado, selection, 'No se encontr√≥ un cultivo adecuado.')).

% Regla de recomendaci√≥n por ciclo
recomendar_por_ciclo(Cultivo, Resultado) :-
    cultivo(Cultivo, _, _, _, _, _, Duracion),
    (Duracion < 120 -> 
        send(Resultado, selection, 'Ciclo Corto')
    ;
        send(Resultado, selection, 'Ciclo Largo')).

